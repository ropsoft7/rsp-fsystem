# Definindo variáveis de proxy
unset http_proxy
unset https_proxy
unset ftp_proxy  
unset socks_proxy

# Se você estiver usando o Privoxy, você pode adicionar também
unset HTTP_PROXY
unset HTTPS_PROXY
unset FTP_PROXY
unset SOCKS_PROXY

# Configuração para não usar o proxy para certas URLs
unset no_proxy